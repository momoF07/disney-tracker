def get_ride_image(ride_name):
    """Retourne l'URL de l'image d'une attraction, ou None si pas trouvée."""
    # Recherche exacte
    if ride_name in RIDE_IMAGES:
        return RIDE_IMAGES[ride_name]
    # Recherche approximative (guillemets, accents)
    clean = ride_name.lower().strip('"\u201c\u201d\u2018\u2019')
    for key, url in RIDE_IMAGES.items():
        if key.lower().strip('"\u201c\u201d\u2018\u2019') == clean:
            return url
    return None
